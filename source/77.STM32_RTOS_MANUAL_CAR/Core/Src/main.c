{
	switch (GPIO_Pin)
	{
		case GPIO_PIN_0:    // PA.0
//printf("EXT INT 0\n");
			led_all_off();
			HAL_GPIO_WritePin(GPIOB, 0x01 << 0, GPIO_PIN_SET);
			ext_int_no = GPIO_PIN_0;
			break;
		case GPIO_PIN_1:    // PA.1			led_all_off();
			HAL_GPIO_WritePin(GPIOB, 0x01 << 1, GPIO_PIN_SET);
//printf("EXT INT 1\n");
			ext_int_no = GPIO_PIN_1;
			break;
	}
}
